# Case

num = 'four'

case num

when "one"
    puts 'num is 1'

when "two"
    puts 'num is 2'

when "three"
    puts 'num is 3'

when "four"
    puts 'num is 4'

else
    puts 'Default'

end

str = 'four'
# If Else If
if str == 'one'
    puts 'num is 1'

elsif str == 'four'
    puts 'num is 4'

else 
    puts 'not in options.'
end
